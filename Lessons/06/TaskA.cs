        public class MorseCode
        {
            private readonly string _morseCodeWithDelimeters;

            private readonly string _morseDelimiter;

            public MorseCode(string morseCodeWithDelimiters, string morseDelimiter = MorseCodeConstants.MorseCodesSeparator)
            {
                _morseCodeWithDelimeters = morseCodeWithDelimiters;
                _morseDelimiter = morseDelimiter;
            }

            public static implicit operator MorseCode(string text)
            {
                var morseCodeWithDelimeters = MorseCodeConverter.ToMorseCode(text);

                return new MorseCode(morseCodeWithDelimeters);
            }

            public static implicit operator string(MorseCode morseCode)
            {
                var text = MorseCodeConverter.FromMorseCode(morseCode._morseCodeWithDelimeters,
                    morseCode._morseDelimiter);

                return text;
            }
        }

        public static class MorseCodeConverter
        {
            private static readonly Dictionary<char, string> _characterToMorse;

            private static readonly Dictionary<string, char> _morseToCharacter;

            static MorseCodeConverter()
            {
                _characterToMorse =
                    JsonConvert.DeserializeObject<Dictionary<char, string>>(MorseCodeConstants.MorseAlphabetAsJson);

                _morseToCharacter = _characterToMorse.ToDictionary(x => x.Value, x => x.Key);
            }

            public static string ToMorseCode(string text, string morseDelimiter = MorseCodeConstants.MorseCodesSeparator)
            {
                return string.Join(morseDelimiter,
                    text.Select(c => _characterToMorse[char.ToLower(c)]));
            }

            public static string FromMorseCode(string morseCode, string morseDelimiter = MorseCodeConstants.MorseCodesSeparator)
            {
                return string.Join(string.Empty,
                    morseCode.Split(new[] { morseDelimiter }, StringSplitOptions.RemoveEmptyEntries)
                        .Select(str => _morseToCharacter[str]));
            }
        }

